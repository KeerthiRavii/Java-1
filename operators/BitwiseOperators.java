package keerthiravi.operators;

public class BitwiseOperators {
	
public static void main(String[] args) {
		
		int r=54;
		int s=67;
		
		System.out.println("r&s="+(r&s));
		System.out.println("r|s="+(r|s));
		
		int r1=0;
		int s1=0;
		
		System.out.println("r&s="+(r1&s1));
		System.out.println("r|s="+(r1|s1));
		
		
		int r11=1;
		int s11=1;
		
		System.out.println("r&s="+(r11&s11));
		System.out.println("r|s="+(r11|s11));

		
}	

}
