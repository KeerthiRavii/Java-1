package keerthiravi.operators;

public class ConditionalOperators {

	public static void main(String[] args) {
		
		int r=54;
		int s=67;
		
		if((r==54) && (s==67))
			System.out.println("r and s is equal");
		else
			System.out.println("not equal");
	
		if((r==54)||(s==67))
			System.out.println("equal");
		else
			System.out.println("no");
		

		if((r==54) & (s==54))
			System.out.println("r and s is equal");
		else
			System.out.println("not equal");
	
		if((r==54)||(s==54))
			System.out.println("equal");
		else
			System.out.println("no");
		

		if((r==54) & (s==67))
			System.out.println("r and s is equal");
		else
			System.out.println("not equal");
	
		if((r==54)||(s==54))
			System.out.println("equal");
		else
			System.out.println("no");
		

		if((r==54) & (s==54))
			System.out.println("r and s is equal");
		else
			System.out.println("not equal");
	
		if((r==54)||(s==67))
			System.out.println("equal");
		else
			System.out.println("no");
	}	
	
}
	
