package keerthiravi.operators;

public class UnaryOperators {
	
	public static void main(String[] args) {
		
		int r = +1;
		System.out.println(r);
		
		r--;
		System.out.println(r);
		
	    r++;
		System.out.println(r);
		
		r =-r;
		System.out.println(r);
		
		boolean success=false;
		System.out.println(success);
		System.out.println(!success);
		
		int i=3;
		i++;
        System.out.println(i);
        
        ++i;
        System.out.println(i);
        
        System.out.println(++i);
        System.out.println(i++);
        
        System.out.println(--i);
        System.out.println(i--);
        
		
		
	}	
	
}
		

