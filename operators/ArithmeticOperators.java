package keerthiravi.operators;

public class ArithmeticOperators {
   
	public static void main(String[] args) {
		
		int r=1+2;
		System.out.println("1+2"+r);
		
		int original_r=r;
		r=r-1;
		System.out.println(original_r+"-1="+r);
		
	    original_r=r;
		r=r*2;
		System.out.println(original_r+"*2="+r);
		
	    original_r=r;
		r=r/2;
		System.out.println(original_r+"/2="+r);
		
		original_r=r;
		r=r%2;
		System.out.println(original_r+"%2="+r);
		
		
		
		

	}
		
}
